let num = 5;

num += 3;  // num = num + 3 -> 8
console.log("+=:", num);

num -= 2;  // num = num - 2 -> 6
console.log("-=: ", num);

num *= 2;  // num = num * 2 -> 12
console.log("*=: ", num);

num /= 3;  // num = num / 3 -> 4
console.log("/=: ", num);

num %= 3;  // num = num % 3 -> 1 (remainder)
console.log("%=: ", num);
