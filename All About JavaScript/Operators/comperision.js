let x = 10;
let y = 20;

console.log("Equal:", x == y);         // false (x is not equal to y)
console.log("Not Equal:", x != y);     // true (x is not equal to y)
console.log("Strict Equal:", x === 10); // true (x is strictly equal to 10)
console.log("Strict Not Equal:", x !== 10); // false (x is strictly equal to 10)
console.log("Greater Than:", x > y);   // false (10 is not greater than 20)
console.log("Less Than:", x < y);      // true (10 is less than 20)
console.log("Greater or Equal:", x >= y); // false (10 is not greater or equal to 20)
console.log("Less or Equal:", x <= y);  // true (10 is less or equal to 20)
